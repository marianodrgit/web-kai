function setLanguage(lang) {
    document.documentElement.lang = lang;
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key]; // Using innerHTML to preserve spans inside if any
        }
    });
    // Update active state of language selector
    const btnES = document.getElementById('btn-lang-es');
    const btnEN = document.getElementById('btn-lang-en');
    const btnESMob = document.getElementById('btn-lang-es-mob');
    const btnENMob = document.getElementById('btn-lang-en-mob');

    if (btnES && btnEN) {
        if (lang === 'en') {
            btnEN.classList.add('font-bold', 'text-primary');
            btnEN.classList.remove('text-dark-navy/50');
            btnES.classList.remove('font-bold', 'text-primary');
            btnES.classList.add('text-dark-navy/50');

            if (btnENMob) {
                btnENMob.classList.add('font-bold', 'text-primary');
                btnESMob.classList.remove('font-bold', 'text-primary');
            }
        } else {
            btnES.classList.add('font-bold', 'text-primary');
            btnES.classList.remove('text-dark-navy/50');
            btnEN.classList.remove('font-bold', 'text-primary');
            btnEN.classList.add('text-dark-navy/50');

            if (btnESMob) {
                btnESMob.classList.add('font-bold', 'text-primary');
                btnENMob.classList.remove('font-bold', 'text-primary');
            }
        }
    }
    localStorage.setItem('kairos_lang', lang);

    // Update Hero Asset (Image or Video)
    const heroAsset = document.getElementById('hero-asset');
    const heroVideo = document.getElementById('hero-video');
    
    if (heroAsset && translations[lang].hero_asset) {
        heroAsset.src = translations[lang].hero_asset;
    }
    
    if (heroVideo && translations[lang].hero_video) {
        const source = heroVideo.querySelector('source');
        if (source) {
            source.src = translations[lang].hero_video;
            heroVideo.load();
        }
    }

    // Update Gallery
    for (let i = 1; i <= 5; i++) {
        const gallImg = document.getElementById(`gallery-${i}`);
        if (gallImg && translations[lang][`gall_${i}`]) {
            gallImg.src = translations[lang][`gall_${i}`];
        }
    }
}

// Initialization
document.addEventListener('DOMContentLoaded', () => {
    // 1. Check local storage
    let lang = localStorage.getItem('kairos_lang');
    // 2. Check browser language
    if (!lang) {
        const userLang = navigator.language || navigator.userLanguage;
        lang = userLang.startsWith('es') ? 'es' : 'en';
    }
    setLanguage(lang);
});
